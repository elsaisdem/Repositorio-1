def validar_contraseña():
    contraseña_almacenada = "elsa2024"
    intentos_maximos = 3
    intentos_realizados = 0
    while intentos_realizados < intentos_maximos:
        contraseña_ingresada = input("Ingrese la contraseña: ")
        if contraseña_ingresada == contraseña_almacenada:
            print("FELICITACIONES")
            return
        intentos_realizados += 1
        if intentos_realizados < intentos_maximos:
            print(f"Contraseña incorrecta. Intento {intentos_realizados} de {intentos_maximos}.")
    print("CONTRASEÑA BLOQUEADA")
validar_contraseña()

