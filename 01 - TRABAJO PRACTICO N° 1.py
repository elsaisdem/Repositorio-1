def contar_positivos_y_total():
    numeros = []
    while True:
        numero_str = input("Ingrese un número real (o 'fin' para terminar): ")
        if numero_str.lower() == 'fin':
            break
        try:
            numero = float(numero_str)
            if numero != 0:
                numeros.append(numero)
        except ValueError:
            print("Entrada inválida. Ingrese un número real válido o 'fin' para terminar.")
    contador_positivos = sum(1 for num in numeros if num > 0)
    total_numeros = len(numeros)
    print(f"Se ingresaron un total de {total_numeros} números.")
    print(f"De los cuales, {contador_positivos} números son positivos.")
contar_positivos_y_total()

def es_palindromo(cadena):
    cadena = cadena.lower().replace(" ", "")
