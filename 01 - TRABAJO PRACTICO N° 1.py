def determinar_grupo(nombre, sexo):
    nombre = nombre.lower()
    sexo = sexo.lower()
    if sexo == 'mujer':
        if nombre < 'm':
            return 'A'
        else:
            return 'B'
    elif sexo == 'hombre':
        if nombre > 'n':
            return 'A'
        else:
            return 'B'
    else:
        return 'Grupo no definido'
def main():
    nombre = input("Ingrese su nombre: ")
    sexo = input("Ingrese su sexo (Hombre/Mujer): ")
    grupo = determinar_grupo(nombre, sexo)
    if grupo == 'A':
        print("Usted pertenece al Grupo A.")
    elif grupo == 'B':
        print("Usted pertenece al Grupo B.")
    else:
        print("No se pudo determinar el grupo.")
if __name__ == "__main__":
    main()

